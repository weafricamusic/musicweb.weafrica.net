-- Backfill: subscription_id/start_date/end_date from existing columns
do $$
declare
	us_type text;
	plans_type text;
begin
	select data_type into us_type
	from information_schema.columns
	where table_schema = 'public'
		and table_name = 'user_subscriptions'
		and column_name = 'subscription_id';

	select data_type into plans_type
	from information_schema.columns
	where table_schema = 'public'
		and table_name = 'subscription_plans'
		and column_name = 'subscription_id';

	if us_type is not null and plans_type is not null and us_type = plans_type then
		update public.user_subscriptions us
		set
			start_date = coalesce(us.start_date, us.started_at),
			end_date = coalesce(us.end_date, us.ends_at),
			subscription_id = coalesce(us.subscription_id, sp.subscription_id)
		from public.subscription_plans sp
		where us.plan_id = sp.plan_id;
	else
		raise notice 'Skipping subscription_id backfill due to incompatible types: % vs %', us_type, plans_type;
		update public.user_subscriptions us
		set
			start_date = coalesce(us.start_date, us.started_at),
			end_date = coalesce(us.end_date, us.ends_at)
		where us.started_at is not null or us.ends_at is not null;
	end if;
end $$;
-- Compatibility trigger:
-- - ensures start_date/end_date stay in sync with started_at/ends_at
-- - auto-populates subscription_id based on plan_id when possible
create or replace function public.tg_user_subscriptions_compat()
returns trigger
language plpgsql
as $$
begin
	if new.start_date is null and new.started_at is not null then
		new.start_date = new.started_at;
	end if;

	if new.started_at is null and new.start_date is not null then
		new.started_at = new.start_date;
	end if;

	if new.end_date is null and new.ends_at is not null then
		new.end_date = new.ends_at;
	end if;

	if new.ends_at is null and new.end_date is not null then
		new.ends_at = new.end_date;
	end if;

	if new.subscription_id is null and new.plan_id is not null then
		select sp.subscription_id
			into new.subscription_id
		from public.subscription_plans sp
		where sp.plan_id = new.plan_id
		limit 1;
	end if;

	return new;
end;
$$;
do $$
begin
	if not exists (
		select 1 from pg_trigger
		where tgname = 'user_subscriptions_compat'
	) then
		create trigger user_subscriptions_compat
		before insert or update on public.user_subscriptions
		for each row
		execute function public.tg_user_subscriptions_compat();
	end if;
end $$;
do $$
begin
	if not exists (

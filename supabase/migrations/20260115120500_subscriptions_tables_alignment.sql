	end if;
end $$;
-- 4) C) Optional per-content access mapping
-- NOTE: The name `subscription_content_access` is already used for JSON rules per plan.
-- This table provides the requested per-content mapping without colliding.
do $$
declare
	subs_type text;
begin
	select data_type into subs_type
	from information_schema.columns
	where table_schema = 'public'
		and table_name = 'subscriptions'
		and column_name = 'id';

	if subs_type is null then
		subs_type := 'integer';
	end if;

	execute format(
		'create table if not exists public.subscription_content_access_items (
			id integer generated by default as identity primary key,
			subscription_id %s not null references public.subscriptions (id) on delete cascade,
			content_type text not null check (content_type in (''songs'',''videos'')),
			content_id uuid not null,
			access_level text not null default ''full'' check (access_level in (''full'',''limited'')),
			created_at timestamptz not null default now(),
			updated_at timestamptz not null default now()
		)',
		subs_type
	);
end $$;
alter table public.subscription_content_access_items enable row level security;
do $$
begin
	if not exists (
		select 1
		from pg_policies
		where schemaname = 'public'
			and tablename = 'subscription_content_access_items'
			and policyname = 'deny_all_subscription_content_access_items'
	) then
		create policy deny_all_subscription_content_access_items
			on public.subscription_content_access_items
			for all
			using (false)
			with check (false);
	end if;
end $$;
create index if not exists subscription_content_access_items_subscription_idx
	on public.subscription_content_access_items (subscription_id);
create index if not exists subscription_content_access_items_content_idx
	on public.subscription_content_access_items (content_type, content_id);
-- Enforce content existence based on content_type (trigger-based integrity)
create or replace function public.tg_validate_subscription_content_access_item()
returns trigger
language plpgsql
as $$
begin
	if new.content_type = 'songs' then
		if not exists (select 1 from public.songs where id = new.content_id) then
			raise exception 'Invalid content_id %, no row in public.songs', new.content_id;
		end if;
	elsif new.content_type = 'videos' then
		if not exists (select 1 from public.videos where id = new.content_id) then
			raise exception 'Invalid content_id %, no row in public.videos', new.content_id;
		end if;
	else
		raise exception 'Invalid content_type %', new.content_type;
	end if;
	return new;
end;
$$;
do $$
begin
	if not exists (
		select 1
		from pg_trigger
		where tgname = 'subscription_content_access_items_validate'
	) then
		create trigger subscription_content_access_items_validate
		before insert or update on public.subscription_content_access_items
		for each row
		execute function public.tg_validate_subscription_content_access_item();
	end if;

	if not exists (
		select 1 from pg_trigger
		where tgname = 'subscription_content_access_items_set_updated_at'
	) then
		create trigger subscription_content_access_items_set_updated_at
		before update on public.subscription_content_access_items
		for each row
		execute function public.tg_set_updated_at();
	end if;
end $$;
